export function getAircraftTypeName(icaoTypeCode: string | null | undefined): string {
  if (!icaoTypeCode) return 'Unknown';
  const code = icaoTypeCode.trim().toUpperCase();
  return ICAO_TYPE_NAMES[code] || code;
}
